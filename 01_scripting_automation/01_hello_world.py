"""
Script: Hello World
What it does: The most basic Python program — prints a message to the screen.
This is always the first program beginners write in any language.
"""

# print() is a built-in function that displays text on the screen
print("Hello, World!")

# You can also print variables
name = "Python"  # This is a string variable
print("Hello,", name)

# You can use f-strings (formatted strings) to embed variables inside text
message = f"Welcome to {name} programming!"
print(message)
